        <h1>Info</h1>
        <p>Mi, Bemuf Göteborg, zajedno sa Islamic Reliefom i Islamskom zajednicom Bosnjaka Göteborg pokrenuli smo projekat u Bosni i Hercegovini čiji je cilj renoviranje sirotišta ”JU DOM-porodica” u Zenici. </p>
    
        <p>Kratki opis doma i stanja u kojem se nalazi ovaj dom za jetime.</p>
        
        <p>JU Dom-porodica Zenica osnovan je 1960 godine u svrhu zbrinjavanja djece bez adekvatnog roditeljskog staranja. Dodatno, djelatnost je proširena na pružanje usluga smještaja samohranih, nezaposlenih trudnica i samohranih, nezaposlenih majki sa djecom do 12 mjeseci starosti. Također, ova ustanova brine se za pripremu i distribuciju 820-840 obroka dnevno za štićenike Gradske narodne kuhinje. Ustanova zbrinjava u prosjeku 84 djece. Ove godine ova ustanova ima i 6 stićenika studenata, te nekoliko nezaposlenih majki s bebama. Ustanova broji 64 zaposlena radnika, većinu čine vaspitači, stručni saradnici, medicinsko osoblje i drugi saradnici u radu s djecom. Budući da se Ustanova finansira iz vrlo ograničenih i nedovoljnih sredstava budžeta Grada Zenica i Zeničko-dobojskog kantona, te iz donacija, ova Ustanova vrlo teško iznalazi sredstva za ulaganja u objekat i opremu. Zgrada u cjelosti nije renovirana nikada, nemam vanjsku toplotnu izolaciju- termofasadu, još uvijek nisu uspjeli zamijeniti svu vanjsku stolariju, unutrašnja stolarija nije mijenjana nikako, nemaju lift iako imaju štićenike s poteškoćama u kretanju. Najznačajnija oprema koja ih služi i koja se sve teže popravlja datira iz 1996 god. pa i starije, te je u prosjeku stara preko 20 godina. Također, najveći troškovi i njihova najznačajna ulaganja uvijek idu u kuhinju jer se u njoj u prosjeku spremi 1.070 obroka dnevno, 250 obroka dnevno za štićenike JU Dom-porodica, ostalo za korisnike Gradske narodne kuhinje.</p>
        
        <p>Bemuf Göteborg bi sa ovim projektom pomogao ovoj ustanovi da rijesi najpotrebnije stavri koje ustanova u ovom trenutku treba; renovacija kuhinje, veseraja, te glavnog hodnika i hola na ulazu u Ustanovu. Sredstva koja su potrebna za renovaciju i realizaciju projekta iznose 500 000 kr ili 100 000 KM.</p>
        
        <p>Bemuf Göteborg ce da organizuje donatorsku veceru 12. novembra u svrhu prikupljanja sredstava za realizaciju projekta. Cilj na je na ovoj donatorkoj veceri prikupiti 250 000 kr.</p>
        
        <p>Želite li pomoći sponzorstvu ovog projekta? Donacijom ili na drugi način podržavajući ovaj projekat, (npr. plaćanjem dijela ili cijele večere) možemo ponuditi reklamiranje vaše kompanije. To bi se uradilo tako što bi vaš logo bio vidljiv na donjem dijelu naših bilborda koji će biti rasprostranjeni po cijeloj Švedskoj, te da možemo postaviti roll-up postere na donatorskoj večeri na kojoj će biti prisutno oko 300 ljudi.</p>
        
