        <p>Šta je Bemuf?</p>
        
        <p>Bemuf je osnovana omladinska organizacija u Švedskoj čiji je cilj očuvanje i razvoj vjerskih, demokratskih, moralnih i kulturnih vrijednosti kod mladih, kao i ravnopravnost polova. Bemuf svoje djelovanje zasniva na Kuranu i Sunnetu.</p>
        
